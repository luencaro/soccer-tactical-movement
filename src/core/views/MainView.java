    private void start_btnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_start_btnMousePressed
        // TODO add your handling code here:
        
        Response response = PlayerLoaderController.loadPlayersFromCSV(file, field);
        
        if (response.getStatus() >= 500) {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Error " + response.getStatus(), JOptionPane.ERROR_MESSAGE);
        } else if (response.getStatus() >= 400) {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Error " + response.getStatus(), JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Response Message", JOptionPane.INFORMATION_MESSAGE);
            DaPanel.removeAll();
            DaPanel.add(Strat1);
            DaPanel.repaint();
            DaPanel.revalidate();
        }
    }//GEN-LAST:event_start_btnMousePressed

    private void upload_btnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_upload_btnMousePressed
        // TODO add your handling code here
        // Crear un JFileChooser
        fileChooser = new JFileChooser();
        
        // Mostrar el di√°logo para seleccionar archivos
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            // Obtener el archivo seleccionado
            File selectedFile = fileChooser.getSelectedFile();
            file = selectedFile.getAbsolutePath().replace("\\", "/");
        }
        text_lbl.setText(file);
    }//GEN-LAST:event_upload_btnMousePressed

